  (MAXIMUM_FREQUENCY - MINIMUM_FREQUENCY) /
  (SATURATION_THRESHOLD - ACTIVATION_THRESHOLD);

export const Y_INTERCEPT = MAXIMUM_FREQUENCY - SLOPE * SATURATION_THRESHOLD;
